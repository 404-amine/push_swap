#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dup;
	size_t	lenght;
	size_t	i;

	lenght = ft_strlen(s);
	i = 0;
	dup = malloc((lenght + 1) * sizeof(char));
	if (dup == NULL)
		return (NULL);
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
